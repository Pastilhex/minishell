#include "../includes/minishell.h"

bool	ft_is_absolute_path(char *str)
{
	if (!str)
		return (false);
	if (str[0] == '/' || (str[0] == '.' && str[1] == '/')
		|| (str[0] == '.' && str[1] == '.' && str[2] == '/')
		|| (str[0] == '~' && str[1] == '/'))
		return (true);
	return (false);
}
